drop PROCEDURE DT40903.SPGETDESTLOC_AFT;
CREATE PROCEDURE DT40903.SPGETDESTLOC_AFT( 
	IN P_SCHEMA CHAR(10) , 
	IN P_BATCH_ID INTEGER,
	IN P_PROC_FLAG CHAR(1),
	IN P_LST_PHYSFILE_ID CHAR(14) ) 
	DYNAMIC RESULT SETS 1 
	LANGUAGE SQL 
	BEGIN 
	 
	DECLARE C_DESTLOC_1 CURSOR WITH RETURN FOR 
	SELECT
		trim(dl.SCHEMA_NM) as SCHEMA_NM,
		trim(dl.BATCH_ID) as BATCH_ID,
		trim(dl.PROCESS_FLAG) as PROCESS_FLAG,
		trim(dl.ACTION_FLAG) as ACTION_FLAG,
		trim(dl.PHYS_FILE_ID) as PHYS_FILE_ID,
		trim(dl.FRM_DEST_ID) as FRM_DEST_ID,
		trim(dl.FRM_LOC) as FRM_LOC,
		trim(dl.TO_DEST_ID) as TO_DEST_ID,
		trim(dl.TO_LOC) as TO_LOC
	FROM 
		DT40903.DEST_LOC dl
	WHERE 
		dl.SCHEMA_NM = P_SCHEMA 
	 	AND dl.BATCH_ID = P_BATCH_ID 
	 	AND dl.PROCESS_FLAG = P_PROC_FLAG 
	 ORDER BY PHYS_FILE_ID ASC 
	FETCH FIRST 5000 ROWS ONLY FOR READ ONLY WITH UR; 
  
	DECLARE C_DESTLOC_2 CURSOR WITH RETURN FOR 
	SELECT
		trim(dl.SCHEMA_NM) as SCHEMA_NM,
		trim(dl.BATCH_ID) as BATCH_ID,
		trim(dl.PROCESS_FLAG) as PROCESS_FLAG,
		trim(dl.ACTION_FLAG) as ACTION_FLAG,
		trim(dl.PHYS_FILE_ID) as PHYS_FILE_ID,
		trim(dl.FRM_DEST_ID) as FRM_DEST_ID,
		trim(dl.FRM_LOC) as FRM_LOC,
		trim(dl.TO_DEST_ID) as TO_DEST_ID,
		trim(dl.TO_LOC) as TO_LOC
	FROM 
		DT40903.DEST_LOC dl
	WHERE 
		dl.SCHEMA_NM = P_SCHEMA 
	 	AND dl.BATCH_ID = P_BATCH_ID 
	 	AND dl.PROCESS_FLAG = P_PROC_FLAG  
	 	AND PHYS_FILE_ID > P_LST_PHYSFILE_ID 
	 ORDER BY PHYS_FILE_ID ASC 
	FETCH FIRST 5000 ROWS ONLY FOR READ ONLY WITH UR; 
	 
	IF ( P_LST_PHYSFILE_ID IS NULL ) THEN 
		OPEN C_DESTLOC_1; 
	ELSE	 
		OPEN C_DESTLOC_2; 
	END IF ; 
END  ; 
  
COMMENT ON PARAMETER SPECIFIC PROCEDURE DT40903.SPGETDESTLOC_AFT 
( 
	P_SCHEMA IS 'SCHEMA of DEST_LOC', 
	P_BATCH_ID IS 'BATCH of DEST_LOC',
	P_PROC_FLAG IS ' PROCESS FLAG VALUE',
	P_LST_PHYSFILE_ID IS 'Last PHYS_FILE_ID Processed'
) ;

