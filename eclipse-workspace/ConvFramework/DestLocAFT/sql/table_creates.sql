CREATE PROCEDURE DT40903.SPGET_DESTINCT_DESTIDS( ) 
	DYNAMIC RESULT SETS 1 
	LANGUAGE SQL 
	BEGIN 
	 
	DECLARE C_DESTID CURSOR WITH RETURN FOR 
	WITH FIND_DEST as
	(
		SELECT 
			COUNT(*),FRM_DEST_ID AS DESTID 
		FROM 
			DT40903.DEST_LOC 
		GROUP BY FRM_DEST_ID 
		UNION ALL
		SELECT 
			COUNT(*),TO_DEST_ID AS DESTID 
		FROM 
			DT40903.DEST_LOC 
		WHERE 
			TO_DEST_ID IS NOT NULL 
		GROUP BY TO_DEST_ID 
	)
	SELECT DISTINCT TRIM(DESTID) AS DEST_ID FROM FIND_DEST FOR READ ONLY WITH UR;
	
	OPEN C_DESTID; 
	 
END; 

